package main

import "fmt"

func reverse(text string) string {
	runes := []rune(text)
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	return string(runes)
}

func main() {
	input := "golang"
	fmt.Println("Original:", input)
	fmt.Println("Reversed:", reverse(input))
}
